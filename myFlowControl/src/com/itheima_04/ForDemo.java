package com.itheima_04;
/*
 * for 循环语句的格式：
 * 			for(初始化语句; 判断条件语句; 控制条件语句){
 * 				循环体语句;
 * 		}
 */
public class ForDemo {
	public static void main(String[] args) {
		//原始方法
		System.out.println("HelloWorld");
		System.out.println("HelloWorld");
		System.out.println("HelloWorld");
		System.out.println("HelloWorld");
		System.out.println("HelloWorld");
		System.out.println("HelloWorld");
		System.out.println("HelloWorld");
		System.out.println("HelloWorld");
		System.out.println("HelloWorld");
		System.out.println("HelloWorld");
		System.out.println("----------------------");
		
		//for循环实现
		for (int i = 0; i < 10; i++) {
			System.out.println("HelloWorld");
		}
	}
}
