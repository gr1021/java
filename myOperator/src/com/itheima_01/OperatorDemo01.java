package com.itheima_01;
/*
 *运算符：是将变量与常量连接起来的符号
 *		主要分为：
 *				算数运算符
 *				赋值运算符
 *				关系运算符
 *				逻辑运算符
 *				三元运算符
 *算数运算符：+ - * / % ++ -- 
 *	
 */
public class OperatorDemo01 {
	public static void main(String[] args) {
		int a = 3;
		int b = 4;
		System.out.println(a + b);
		System.out.println(a - b);
		System.out.println(a * b);
		System.out.println(a / b);
		
	}
}
