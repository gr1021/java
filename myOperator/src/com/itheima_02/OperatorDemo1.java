package com.itheima_02;
/*
 * 赋值运算符：
 * 			基本赋值运算符 =
 * 			扩展运算符 += -= *= 、/= 
 */
public class OperatorDemo1 {
	public static void main(String[] args) {
		//=  基本赋值运算符
		int a = 10;
		int b = 20;
		//+=  
		a += 20;
		System.out.println(a);
		//-= 
		b -= 10;
		System.out.println(b);
		//*= 
		a *= 2;
		System.out.println(a);
		// /=
		b /= 3;
		System.out.println(b);
			
	}
}
